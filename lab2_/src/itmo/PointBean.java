package itmo;

public class PointBean {
    private double x,y,r;
    private String res;

    public double getY() {
        return y;
    }

    public double getR() {
        return r;
    }

    public String getRes() {
        return res;
    }

    public double getX() { return x; }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setR(double r) {
        this.r = r;
    }

    public void setRes(String res) {
        this.res = res;
    }
}
